    /**
     * 简体 转 繁体
     * @param string $string
     * @return string
     */
    public function J2F(string $string): string
    {
        return self::chs_to_cht($string);
    }

    /**
     * 繁体 转 简体
     * @param string $string
     * @return string
     */
    public function F2J(string $string): string
    {
        return self::cht_to_chs($string);
    }


    /**
     * 简体 转 繁体
     * @param string $string
     * @return string
     */
    public static function chs_to_cht(string $string): string
    {
        if (empty(trim($string))) return '';

        $array = self::chs_to_cht_arr();

        return self::change($string, $array);
    }

    /**
     * 繁体 转 简体
     * @param string $string
     * @return string
     */
    public static function cht_to_chs(string $string): string
    {
        if (empty(trim($string))) return '';

        $array = self::cht_to_chs_arr();

        return self::change($string, $array);
    }

    /**
     * 转换数据
     * @param string $string
     * @param array  $array
     * @return string
     */
    private static function change(string $string, array $array): string
    {
        $keys = array_keys($array);
        $values = array_values($array);
        $output = str_replace($keys, $values, $string);
        return $output;
    }


